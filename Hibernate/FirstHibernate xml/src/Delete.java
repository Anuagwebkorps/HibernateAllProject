import java.util.Scanner;

import org.hibernate.Session;
import org.hibernate.SessionFactory;
import org.hibernate.Transaction;
import org.hibernate.cfg.Configuration;

import pp.Emp;

public class Delete {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter User id:");
		int id =sc.nextInt();
		
	
		Session session=new Configuration().configure().buildSessionFactory().openSession();
		Transaction ex=session.beginTransaction(); //Transaction use kerte werna roll back hojata
		Emp e=new Emp();
		e.setId(id);
		//session.persist(e);
		session.delete(e); //save method return primary key //insert kerne ke liye save method call hoti hai
		ex.commit(); //commit se query to run ho jaye gi but data save nahi hoga rollback ho jata hai
		session.close();
		System.out.println("Delete");
		//Drawback of delete metod is that it return void method so we dont identifay that data delete or note
	}

}











































































































































































































































































































































































































































